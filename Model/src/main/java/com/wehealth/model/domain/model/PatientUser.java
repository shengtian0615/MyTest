package com.wehealth.model.domain.model;

public class PatientUser {

	private String phoneId;
	private String inputIdCardNo;
	private int age;
	private int gender;
	private String name;
	private String str;
	private String msg;
	private String serialNo;
	private String registerId;
	private long time;
	private long updataTime;
	
	public String getPhoneId() {
		return phoneId;
	}
	public void setPhoneId(String phoneId) {
		this.phoneId = phoneId;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
	public String getRegisterId() {
		return registerId;
	}
	public void setRegisterId(String registerId) {
		this.registerId = registerId;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public long getUpdataTime() {
		return updataTime;
	}
	public void setUpdataTime(long updataTime) {
		this.updataTime = updataTime;
	}
	public String getInputIdCardNo() {
		return inputIdCardNo;
	}
	public void setInputIdCardNo(String inputIdCardNo) {
		this.inputIdCardNo = inputIdCardNo;
	}
}
